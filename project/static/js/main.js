let currentLanguage = "uz";

const THEME_KEY = "cv-maker-theme";

function applyTheme(theme) {
    const normalized = theme === "dark" ? "dark" : "light";
    document.documentElement.dataset.theme = normalized;
    localStorage.setItem(THEME_KEY, normalized);

    document.querySelectorAll("[data-theme-toggle]").forEach((button) => {
        button.textContent = normalized === "dark" ? "☀️" : "🌙";
        button.setAttribute("aria-label", normalized === "dark" ? "Switch to light mode" : "Switch to dark mode");
    });
}

function toggleTheme() {
    applyTheme(document.documentElement.dataset.theme === "dark" ? "light" : "dark");
}

function initTheme() {
    document.querySelectorAll("[data-theme-toggle]").forEach((button) => {
        button.addEventListener("click", toggleTheme);
    });

    applyTheme(localStorage.getItem(THEME_KEY) || "light");
}

function setLanguage(lang) {
    const dictionary = translations[lang] || translations.en;
    currentLanguage = lang;
    localStorage.setItem("cv-maker-language", currentLanguage);

    document.documentElement.lang = lang;

    if (document.body && document.body.dataset.pageTitleKey) {
        document.title = dictionary[document.body.dataset.pageTitleKey] || document.title;
    }

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;

        if (dictionary[key]) {
            element.textContent = dictionary[key];
        }
    });

    document.querySelectorAll(".lang-btn").forEach((button) => {
        button.classList.toggle("is-active", button.dataset.lang === lang);
    });
}

function initLanguageSwitcher() {
    document.querySelectorAll(".lang-btn").forEach((button) => {
        button.addEventListener("click", () => setLanguage(button.dataset.lang));
    });

    setLanguage(localStorage.getItem("cv-maker-language") || "uz");
}

function initTemplateHover() {
    document.querySelectorAll(".template-preview").forEach((preview) => {
        preview.addEventListener("mouseenter", () => preview.classList.add("is-hovered"));
        preview.addEventListener("mouseleave", () => preview.classList.remove("is-hovered"));
    });
}

function initStartNowScroll() {
    document.querySelectorAll("[data-scroll-to]").forEach((button) => {
        button.addEventListener("click", () => {
            const target = document.querySelector(button.dataset.scrollTo);
            if (target) {
                target.scrollIntoView({ behavior: "smooth", block: "start" });
            }
        });
    });
}

document.addEventListener("DOMContentLoaded", () => {
    initTheme();
    initLanguageSwitcher();
    initTemplateHover();
    initStartNowScroll();
});
