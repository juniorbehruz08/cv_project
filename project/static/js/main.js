let currentLanguage = "en";

function setLanguage(lang) {
    const dictionary = translations[lang] || translations.en;
    currentLanguage = lang;
    localStorage.setItem("cv-maker-language", currentLanguage);

    document.documentElement.lang = lang;
    document.title = dictionary[document.body.dataset.pageTitleKey] || document.title;

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;

        if (dictionary[key]) {
            element.textContent = dictionary[key];
        }
    });

    document.querySelectorAll(".lang-btn").forEach((button) => {
        button.classList.toggle("is-active", button.dataset.lang === lang);
    });
}

document.querySelectorAll(".lang-btn").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.querySelectorAll(".template-preview").forEach((preview) => {
    preview.addEventListener("mouseenter", () => preview.classList.add("is-hovered"));
    preview.addEventListener("mouseleave", () => preview.classList.remove("is-hovered"));
});

setLanguage(localStorage.getItem("cv-maker-language") || "en");
